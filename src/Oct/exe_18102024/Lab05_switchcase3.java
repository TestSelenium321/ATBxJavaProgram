package Oct.exe_18102024;

public class Lab05_switchcase3 {
    public static void main(String[] args) {
        char code = 'B';
        switch (code) {
            default:
                System.out.println("invalid letter");
                break;
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;

        }

        }

}
